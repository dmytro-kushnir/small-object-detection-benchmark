#!/usr/bin/env python3
"""SAHI hyperparameter grid on ants val vs vanilla 768 baseline (no training)."""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
from collections import deque
from copy import deepcopy
from pathlib import Path
from typing import Any


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _load_json(p: Path) -> dict[str, Any]:
    return json.loads(p.read_text(encoding="utf-8"))


def _git_rev(root: Path) -> str | None:
    try:
        return subprocess.check_output(
            ["git", "rev-parse", "HEAD"],
            cwd=root,
            stderr=subprocess.DEVNULL,
            text=True,
        ).strip()
    except (subprocess.CalledProcessError, FileNotFoundError):
        return None


def _resolve_device(repo: Path) -> str:
    import os

    d = os.environ.get("ANTS_DEVICE") or os.environ.get("SMOKE_DEVICE")
    if d and d != "auto":
        return d
    try:
        import torch

        return "0" if torch.cuda.is_available() else "cpu"
    except ImportError:
        return "cpu"


def _pick_weights(yolo_768: Path) -> Path:
    for name in ("best.pt", "last.pt"):
        w = yolo_768 / "weights" / name
        if w.is_file():
            return w
    raise FileNotFoundError(f"No best.pt/last.pt under {yolo_768 / 'weights'}")


def _slim_coco(ce: dict[str, Any]) -> dict[str, Any]:
    keys = (
        "mAP_50_95",
        "mAP_50",
        "mAP_75",
        "mAP_small",
        "mAP_medium",
        "mAP_large",
    )
    return {k: ce.get(k) for k in keys}


def _run(
    repo: Path,
    cmd: list[str],
) -> None:
    r = subprocess.run(cmd, cwd=str(repo))
    if r.returncode != 0:
        sys.exit(r.returncode)


def _build_grid() -> list[dict[str, Any]]:
    out: list[dict[str, Any]] = []
    for psp in (True, False):
        for sz in (512, 640, 768):
            for ov in (0.10, 0.15, 0.25):
                for ct in (0.25, 0.35, 0.45):
                    out.append(
                        {
                            "perform_standard_pred": psp,
                            "slice_height": sz,
                            "slice_width": sz,
                            "overlap_height_ratio": ov,
                            "overlap_width_ratio": ov,
                            "confidence_threshold": ct,
                        }
                    )
    return out


def _best_run(
    runs: list[dict[str, Any]], key: str, mode: str = "max"
) -> dict[str, Any] | None:
    valid = [r for r in runs if "error" not in r and r.get("coco_eval")]
    if not valid:
        return None

    def score(r: dict[str, Any]) -> float:
        v = r["coco_eval"].get(key)
        if v is None:
            return float("-inf") if mode == "max" else float("inf")
        try:
            return float(v)
        except (TypeError, ValueError):
            return float("-inf") if mode == "max" else float("inf")

    if mode == "max":
        return max(valid, key=score)
    return min(valid, key=score)


def _best_run_min_fp(runs: list[dict[str, Any]]) -> dict[str, Any] | None:
    valid = [r for r in runs if "error" not in r and r.get("matched_pr")]
    if not valid:
        return None

    def sort_key(r: dict[str, Any]) -> tuple[int, float]:
        fp = int(r["matched_pr"].get("fp", 10**9))
        m = float(r["coco_eval"].get("mAP_50_95") or -1.0)
        return (fp, -m)

    return min(valid, key=sort_key)


def _write_summary_md(
    path: Path,
    baseline: dict[str, Any],
    runs: list[dict[str, Any]],
    best_map: dict[str, Any] | None,
    best_med: dict[str, Any] | None,
    best_fp_run: dict[str, Any] | None,
    early_stopped: bool,
    stopped_at_index: int | None,
) -> None:
    b_ce = baseline["coco_eval"]
    b_mp = baseline["matched_pr"]
    b_map = float(b_ce.get("mAP_50_95", 0.0))
    b_med = float(b_ce.get("mAP_medium", 0.0))
    b_fp = int(b_mp.get("fp", 0))

    beats_map = best_map and float(best_map["coco_eval"].get("mAP_50_95", 0)) > b_map
    beats_med = best_med and float(best_med["coco_eval"].get("mAP_medium", 0)) > b_med

    fp_better = (
        best_fp_run is not None and int(best_fp_run["matched_pr"].get("fp", 10**9)) < b_fp
    )
    fp_delta = (
        int(best_fp_run["matched_pr"]["fp"]) - b_fp if best_fp_run else None
    )

    top5 = sorted(
        [r for r in runs if "error" not in r and r.get("coco_eval")],
        key=lambda r: float(r["coco_eval"].get("mAP_medium") or -999.0),
        reverse=True,
    )[:5]

    lines = [
        "# EXP-A003: SAHI ablation summary (ants val)",
        "",
        "Auto-generated by `scripts/evaluation/run_ants_expA003_sahi_ablation.py`.",
        "",
        "## Baseline (vanilla imgsz=768)",
        "",
        f"- Source: `{baseline.get('_path', 'ants_expA002b_imgsz768_metrics.json')}`",
        f"- mAP@[.5:.95]: {b_map:.4f}",
        f"- mAP_medium: {b_med:.4f}",
        f"- Matched FP (IoU≥0.5, score≥0.25): {b_fp}",
        "",
        "## Questions",
        "",
        f"1. **Any SAHI config beats vanilla on mAP@[.5:.95]?** {'Yes' if beats_map else 'No'}",
        f"2. **Any SAHI config beats vanilla on mAP_medium?** {'Yes' if beats_med else 'No'}",
        f"3. **Any config reduces FP vs baseline?** {'Yes' if fp_better else 'No'}",
        (
            f"   - Best-FP run FP = {best_fp_run['matched_pr']['fp'] if best_fp_run else '—'} "
            f"(Δ vs baseline: {fp_delta if fp_delta is not None else '—'})"
            if best_fp_run
            else ""
        ),
        "",
        "## Recommendation",
        "",
    ]

    if beats_map or beats_med:
        lines.append(
            "At least one grid point exceeded the vanilla 768 checkpoint on a primary COCO metric; "
            "inspect `ants_expA003_sahi_ablation.json` for params and consider a narrow follow-up."
        )
    elif fp_better and not beats_med:
        lines.append(
            "Some SAHI settings reduced greedy FP without winning mAP_medium; tiling may still be a precision lever "
            "but does not replace vanilla 768 for localization on this split."
        )
    else:
        lines.append(
            "No setting in this grid beat vanilla 768 on mAP or mAP_medium, and FP did not improve materially. "
            "**Deprioritize SAHI** for this ants recipe unless new data or a much smaller search (e.g. NMS post) is justified."
        )

    lines.extend(
        [
            "",
            "## Top 5 by mAP_medium",
            "",
            "| Rank | std_pred | slice | overlap | conf | mAP@[.5:.95] | mAP_med | FP |",
            "|-----:|:--------:|------:|--------:|-----:|------------:|--------:|---:|",
        ]
    )
    for i, r in enumerate(top5, start=1):
        p = r["params"]
        ce = r["coco_eval"]
        mp = r["matched_pr"]
        lines.append(
            f"| {i} | {p['perform_standard_pred']} | {p['slice_height']} | "
            f"{p['overlap_height_ratio']:.2f} | {p['confidence_threshold']:.2f} | "
            f"{ce.get('mAP_50_95', 0):.4f} | {ce.get('mAP_medium', 0):.4f} | {mp.get('fp', 0)} |"
        )

    lines.extend(
        [
            "",
            "## Run metadata",
            "",
            f"- Early stop triggered: **{early_stopped}**",
        ]
    )
    if early_stopped and stopped_at_index is not None:
        lines.append(f"- Stopped after run index (0-based): {stopped_at_index}")
    lines.append("")

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--repo-root", type=str, default=None, help="Repo root (default: infer)")
    ap.add_argument(
        "--baseline-metrics",
        type=str,
        default="experiments/results/ants_expA002b_imgsz768_metrics.json",
    )
    ap.add_argument(
        "--base-sahi-yaml",
        type=str,
        default="configs/expA003_ants_sahi.yaml",
    )
    ap.add_argument(
        "--scratch-dir",
        type=str,
        default="experiments/yolo/ants_expA003_ablation_scratch",
    )
    ap.add_argument(
        "--out-json",
        type=str,
        default="experiments/results/ants_expA003_sahi_ablation.json",
    )
    ap.add_argument(
        "--out-md",
        type=str,
        default="experiments/results/ants_expA003_sahi_ablation_summary.md",
    )
    ap.add_argument("--device", type=str, default=None)
    ap.add_argument(
        "--early-stop-consecutive",
        type=int,
        default=0,
        help="Stop if last N runs are all below baseline mAP_medium and none beat baseline FP (0=off).",
    )
    ap.add_argument(
        "--max-runs",
        type=int,
        default=None,
        help="Cap number of grid configs (debug smoke).",
    )
    args = ap.parse_args()

    repo = Path(args.repo_root).resolve() if args.repo_root else _repo_root()
    baseline_p = (repo / args.baseline_metrics).resolve()
    base_yaml_p = (repo / args.base_sahi_yaml).resolve()
    scratch = (repo / args.scratch_dir).resolve()
    out_json = (repo / args.out_json).resolve()
    out_md = (repo / args.out_md).resolve()

    if not baseline_p.is_file():
        print(f"Baseline metrics not found: {baseline_p}", file=sys.stderr)
        sys.exit(1)
    if not base_yaml_p.is_file():
        print(f"Base SAHI YAML not found: {base_yaml_p}", file=sys.stderr)
        sys.exit(1)

    baseline_full = _load_json(baseline_p)
    baseline_medium = float(baseline_full.get("coco_eval", {}).get("mAP_medium", -1.0))
    baseline_fp = int(baseline_full.get("matched_pr", {}).get("fp", 0))

    yolo_768 = repo / "experiments/yolo/ants_expA002b_imgsz768"
    try:
        weights = _pick_weights(yolo_768)
    except FileNotFoundError as e:
        print(e, file=sys.stderr)
        sys.exit(1)

    gt = repo / "datasets/ants_yolo/annotations/instances_val.json"
    img_val = repo / "datasets/ants_yolo/images/val"
    manifest = repo / "datasets/ants_yolo/prepare_manifest.json"
    train_cfg = yolo_768 / "config.yaml"
    for p in (gt, img_val, manifest, train_cfg):
        if not p.exists():
            print(f"Missing required path: {p}", file=sys.stderr)
            sys.exit(1)

    import yaml

    base_cfg = yaml.safe_load(base_yaml_p.read_text(encoding="utf-8"))
    if not isinstance(base_cfg, dict):
        print("Base SAHI YAML must be a mapping.", file=sys.stderr)
        sys.exit(1)

    device = args.device if args.device is not None else _resolve_device(repo)
    grid = _build_grid()
    if args.max_runs is not None:
        grid = grid[: max(0, args.max_runs)]

    scratch.mkdir(parents=True, exist_ok=True)
    infer_py = repo / "scripts/inference/infer_sahi_yolo.py"
    eval_py = repo / "scripts/evaluation/evaluate.py"
    py = sys.executable

    runs: list[dict[str, Any]] = []
    window: deque[tuple[float, int]] | None = (
        deque(maxlen=args.early_stop_consecutive) if args.early_stop_consecutive > 0 else None
    )
    early_stopped = False
    stopped_at: int | None = None

    def _rel(p: Path) -> str:
        try:
            return str(p.resolve().relative_to(repo.resolve()))
        except ValueError:
            return str(p)

    for idx, params in enumerate(grid):
        cfg = deepcopy(base_cfg)
        cfg["slice_height"] = params["slice_height"]
        cfg["slice_width"] = params["slice_width"]
        cfg["overlap_height_ratio"] = params["overlap_height_ratio"]
        cfg["overlap_width_ratio"] = params["overlap_width_ratio"]
        cfg["confidence_threshold"] = params["confidence_threshold"]
        cfg["perform_standard_pred"] = params["perform_standard_pred"]
        cfg["yolo_imgsz"] = 768

        yml_path = scratch / f"ablation_{idx:03d}.yaml"
        pred_path = scratch / f"pred_{idx:03d}.json"
        met_path = scratch / f"metrics_{idx:03d}.json"

        yml_path.write_text(yaml.safe_dump(cfg, sort_keys=False, allow_unicode=True), encoding="utf-8")

        n_grid = len(grid)
        p = params
        print(
            f"[ablation {idx + 1}/{n_grid}] infer SAHI → {pred_path.name} | "
            f"slice={p['slice_height']} overlap={p['overlap_height_ratio']:.2f} "
            f"conf={p['confidence_threshold']:.2f} std_pred={p['perform_standard_pred']}",
            flush=True,
        )
        _run(
            repo,
            [
                py,
                str(infer_py),
                "--weights",
                str(weights),
                "--source",
                str(img_val),
                "--coco-gt",
                str(gt),
                "--out",
                str(pred_path),
                "--sahi-config",
                str(yml_path),
                "--device",
                device,
                "--no-progress",
                "--progress-every",
                "100",
            ],
        )

        exp_id = f"EXP-A003-ablation-{idx:03d}"
        print(f"[ablation {idx + 1}/{n_grid}] evaluate → {met_path.name}", flush=True)
        _run(
            repo,
            [
                py,
                str(eval_py),
                "--gt",
                str(gt),
                "--pred",
                str(pred_path),
                "--out",
                str(met_path),
                "--weights",
                str(weights),
                "--images-dir",
                str(img_val),
                "--device",
                device,
                "--experiment-id",
                exp_id,
                "--prepare-manifest",
                str(manifest),
                "--train-config",
                str(train_cfg),
                "--imgsz",
                "768",
                "--skip-inference-benchmark",
            ],
        )

        met = _load_json(met_path)
        ce = met.get("coco_eval") or {}
        mp = met.get("matched_pr") or {}
        rec = {
            "index": idx,
            "experiment_id": exp_id,
            "params": params,
            "coco_eval": _slim_coco(ce),
            "matched_pr": {
                "precision_iou50_score025": mp.get("precision_iou50_score025"),
                "recall_iou50_score025": mp.get("recall_iou50_score025"),
                "tp": mp.get("tp"),
                "fp": mp.get("fp"),
                "fn": mp.get("fn"),
            },
        }
        runs.append(rec)

        try:
            pred_path.unlink(missing_ok=True)
            met_path.unlink(missing_ok=True)
            yml_path.unlink(missing_ok=True)
        except OSError:
            pass

        med = float(ce.get("mAP_medium", -999.0))
        fp = int(mp.get("fp", 10**9))
        if window is not None:
            window.append((med, fp))
            if len(window) == args.early_stop_consecutive:
                below_med = all(m < baseline_medium for m, _ in window)
                no_fp_win = all(f >= baseline_fp for _, f in window)
                if below_med and no_fp_win:
                    early_stopped = True
                    stopped_at = idx
                    break

    baseline_payload = {
        "_path": _rel(baseline_p),
        "coco_eval": _slim_coco(baseline_full.get("coco_eval") or {}),
        "matched_pr": {
            "tp": baseline_full.get("matched_pr", {}).get("tp"),
            "fp": baseline_full.get("matched_pr", {}).get("fp"),
            "fn": baseline_full.get("matched_pr", {}).get("fn"),
            "precision_iou50_score025": baseline_full.get("matched_pr", {}).get(
                "precision_iou50_score025"
            ),
            "recall_iou50_score025": baseline_full.get("matched_pr", {}).get(
                "recall_iou50_score025"
            ),
        },
    }

    best_map = _best_run(runs, "mAP_50_95", "max")
    best_med = _best_run(runs, "mAP_medium", "max")
    best_fp = _best_run_min_fp(runs)

    def _strip_for_json(r: dict[str, Any] | None) -> dict[str, Any] | None:
        if r is None:
            return None
        return {"index": r["index"], "params": r["params"], "coco_eval": r["coco_eval"], "matched_pr": r["matched_pr"]}

    payload = {
        "baseline": baseline_payload,
        "grid": {
            "perform_standard_pred": [True, False],
            "slice_size": [512, 640, 768],
            "overlap_ratio": [0.10, 0.15, 0.25],
            "confidence_threshold": [0.25, 0.35, 0.45],
        },
        "weights": _rel(weights),
        "device": device,
        "git_rev": _git_rev(repo),
        "early_stopped": early_stopped,
        "stopped_at_index": stopped_at,
        "n_runs": len(runs),
        "runs": runs,
        "best_by_mAP_50_95": _strip_for_json(best_map),
        "best_by_mAP_medium": _strip_for_json(best_med),
        "best_by_fp": _strip_for_json(best_fp),
    }

    out_json.parent.mkdir(parents=True, exist_ok=True)
    out_json.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    print(f"Wrote {out_json}")

    _write_summary_md(
        out_md,
        {**baseline_full, "_path": baseline_payload["_path"]},
        runs,
        best_map,
        best_med,
        best_fp,
        early_stopped,
        stopped_at,
    )
    print(f"Wrote {out_md}")


if __name__ == "__main__":
    main()
