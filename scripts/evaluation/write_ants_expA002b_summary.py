#!/usr/bin/env python3
"""Write experiments/results/ants_expA002b_summary.md from sweep JSON + optional baseline/relative."""

from __future__ import annotations

import argparse
import json
import math
import sys
from pathlib import Path
from typing import Any

_SCRIPTS_DIR = Path(__file__).resolve().parents[1]
if str(_SCRIPTS_DIR) not in sys.path:
    sys.path.insert(0, str(_SCRIPTS_DIR))
from repo_paths import path_for_artifact


def _load_json(p: Path) -> dict[str, Any]:
    return json.loads(p.read_text(encoding="utf-8"))


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _fmt(x: Any, digits: int = 4) -> str:
    if isinstance(x, (int, float)) and not isinstance(x, bool):
        if isinstance(x, float) and (x != x):
            return "nan"
        return f"{x:.{digits}f}" if isinstance(x, float) else str(x)
    return str(x) if x is not None else "—"


def _map_medium_key(v: Any) -> float:
    try:
        fv = float(v)
    except (TypeError, ValueError):
        return float("-inf")
    if not math.isfinite(fv) or fv < 0:
        return float("-inf")
    return fv


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument(
        "--sweep",
        type=str,
        default="experiments/results/ants_expA002b_resolution_sweep.json",
    )
    p.add_argument(
        "--baseline-metrics",
        type=str,
        default="experiments/results/ants_expA000_full_metrics.json",
        help="Optional; used for comparison (same as 640 sweep when reused)",
    )
    p.add_argument(
        "--relative",
        type=str,
        default="experiments/results/ants_expA002b_relative_metrics.json",
    )
    p.add_argument(
        "--out",
        type=str,
        default="experiments/results/ants_expA002b_summary.md",
    )
    args = p.parse_args()

    sweep_p = Path(args.sweep).expanduser().resolve()
    out_p = Path(args.out).expanduser().resolve()
    root = _repo_root()

    if not sweep_p.is_file():
        print(f"Missing sweep: {sweep_p}", file=sys.stderr)
        sys.exit(1)

    sweep = _load_json(sweep_p)
    summary: list[dict[str, Any]] = list(sweep.get("summary") or [])
    if not summary:
        print("Sweep JSON has empty 'summary' list.", file=sys.stderr)
        sys.exit(1)
    rec = sweep.get("recommendation") or {}
    baseline_p = Path(args.baseline_metrics).expanduser().resolve()
    baseline: dict[str, Any] | None = None
    if baseline_p.is_file():
        baseline = _load_json(baseline_p)

    rel_p = Path(args.relative).expanduser().resolve()
    rel: dict[str, Any] | None = None
    if rel_p.is_file():
        rel = _load_json(rel_p)

    sweep_md = path_for_artifact(sweep_p, root)
    base_md = path_for_artifact(baseline_p, root) if baseline else None
    rel_md = path_for_artifact(rel_p, root) if rel else None

    best_mm = max(summary, key=lambda r: (_map_medium_key(r.get("mAP_medium")), -r["imgsz"]))
    best_fps_row: dict[str, Any] | None = None
    best_fps_v = float("-inf")
    for r in summary:
        try:
            fv = float(r.get("fps"))
        except (TypeError, ValueError):
            continue
        if math.isfinite(fv) and fv > best_fps_v:
            best_fps_v = fv
            best_fps_row = r
    if best_fps_row is None:
        best_fps_row = summary[0]

    lines: list[str] = [
        "# EXP-A002b: Ant dataset resolution sweep",
        "",
        "Auto-generated by `scripts/evaluation/write_ants_expA002b_summary.py`.",
        "",
        "## 1. Inputs",
        "",
        f"- Sweep JSON: `{sweep_md}`",
    ]
    if base_md:
        lines.append(f"- Reference baseline (EXP-A000 full): `{base_md}`")
    if rel_md:
        lines.append(f"- Relative-area aggregate: `{rel_md}`")
    lines.extend(["", "## 2. Metrics by `imgsz`", "", "| imgsz | mAP@[.5:.95] | mAP@.5 | mAP_medium | Precision | Recall | FPS | Latency (ms) |", "|------:|---------------:|-------:|-----------:|----------:|-------:|----:|-------------:|"])

    for r in sorted(summary, key=lambda x: x["imgsz"]):
        lines.append(
            f"| {r['imgsz']} | {_fmt(r.get('mAP'))} | {_fmt(r.get('mAP50'))} | {_fmt(r.get('mAP_medium'))} | "
            f"{_fmt(r.get('precision'))} | {_fmt(r.get('recall'))} | {_fmt(r.get('fps'), 2)} | {_fmt(r.get('latency_ms'), 2)} |"
        )

    lines.extend(
        [
            "",
            "## 3. Best resolution",
            "",
            f"- **Best mAP_medium:** `imgsz={best_mm['imgsz']}` (mAP_medium ≈ {_fmt(best_mm.get('mAP_medium'))}).",
            f"- **Best FPS:** `imgsz={best_fps_row['imgsz']}` (FPS ≈ {_fmt(best_fps_row.get('fps'), 2)}).",
            f"- **Trade-off (median FPS gate, maximize mAP_medium):** `imgsz={rec.get('chosen_imgsz', '—')}` — {rec.get('rule', '')}",
            "",
            "## 4. vs baseline (640, EXP-A000 full)",
            "",
        ]
    )

    if baseline:
        bce = baseline.get("coco_eval") or {}
        bpr = baseline.get("matched_pr") or {}
        bib = baseline.get("inference_benchmark") or {}
        row640 = next((r for r in summary if r["imgsz"] == 640), None)
        lines.append(
            f"Baseline mAP@[.5:.95]: {_fmt(bce.get('mAP_50_95'))}; mAP_medium: {_fmt(bce.get('mAP_medium'))}; "
            f"matched P/R: {_fmt(bpr.get('precision_iou50_score025'))} / {_fmt(bpr.get('recall_iou50_score025'))}; "
            f"FPS ≈ {_fmt(bib.get('fps'), 2)}."
        )
        if row640:
            lines.append(
                f"Sweep row for 640 should match baseline when metrics were reused (mAP {_fmt(row640.get('mAP'))} vs baseline {_fmt(bce.get('mAP_50_95'))})."
            )
        lines.append("")
        lines.append("Higher `imgsz` runs: compare mAP_medium and recall to baseline; expect precision/recall trade-offs in dense ant scenes.")
    else:
        lines.append("*Baseline metrics file not found; skip numeric comparison.*")
        lines.append("")

    lines.extend(
        [
            "## 5. Recall, precision, dense scenes",
            "",
            "- Higher resolution often increases recall on small instances but can add false positives under greedy matched P/R (score≥0.25, IoU≥0.5).",
            "- Dense colonies: watch matched FP/FN in `viz_coco_overlays` comparison panels (orange = missed GT, thick red = FP).",
            "",
        ]
    )

    lines.extend(["## 6. Relative object size (fraction of frame)", ""])
    if rel and isinstance(rel.get("by_imgsz"), dict):
        lines.append("Mean relative bbox area (predictions, score≥threshold) by `imgsz`:")
        lines.append("")
        lines.append("| imgsz | Pred mean rel. area | n_boxes |")
        lines.append("|------:|--------------------:|--------:|")
        for k in sorted(rel["by_imgsz"].keys(), key=lambda x: int(x) if str(x).isdigit() else 0):
            block = rel["by_imgsz"][k]
            pred = (block or {}).get("predictions") or {}
            if "error" in block:
                lines.append(f"| {k} | — | — |")
                continue
            lines.append(
                f"| {k} | {_fmt(pred.get('mean'))} | {pred.get('n_boxes', '—')} |"
            )
        lines.append("")
    else:
        lines.append("*Relative aggregate not found or empty `by_imgsz`.*")
        lines.append("")

    lines.extend(
        [
            "## 7. Conclusion",
            "",
            f"**Recommended starting `imgsz` for ants:** **{rec.get('chosen_imgsz', '—')}** (trade-off rule above), subject to GPU memory and FPS targets.",
            "Validate on your deployment hardware; re-run `./scripts/run_ants_expA002b.sh` after seed or data updates.",
            "",
        ]
    )

    out_p.parent.mkdir(parents=True, exist_ok=True)
    out_p.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {out_p}")


if __name__ == "__main__":
    main()
