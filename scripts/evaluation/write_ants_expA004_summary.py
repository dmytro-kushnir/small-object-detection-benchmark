#!/usr/bin/env python3
"""Write experiments/results/ants_expA004_summary.md from compare JSON + optional rois.json stats."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any

_SCRIPTS_DIR = Path(__file__).resolve().parents[1]
if str(_SCRIPTS_DIR) not in sys.path:
    sys.path.insert(0, str(_SCRIPTS_DIR))
from repo_paths import path_for_artifact


def _load_json(p: Path) -> dict[str, Any]:
    return json.loads(p.read_text(encoding="utf-8"))


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _fmt(x: Any, digits: int = 4) -> str:
    if isinstance(x, (int, float)) and not isinstance(x, bool):
        if isinstance(x, float) and (x != x):
            return "nan"
        return f"{x:.{digits}f}" if isinstance(x, float) else str(x)
    return str(x) if x is not None else "—"


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument(
        "--compare",
        type=str,
        default="experiments/results/ants_expA004_vs_baseline.json",
        help="Output of compare_ants_expA004.py",
    )
    p.add_argument(
        "--rois-json",
        type=str,
        default=None,
        help="Optional experiments/yolo/ants_expA004/rois.json for ROI stats block",
    )
    p.add_argument(
        "--out",
        type=str,
        default="experiments/results/ants_expA004_summary.md",
    )
    args = p.parse_args()

    cmp_p = Path(args.compare).expanduser().resolve()
    out_p = Path(args.out).expanduser().resolve()
    root = _repo_root()

    if not cmp_p.is_file():
        print(f"Missing compare JSON: {cmp_p}", file=sys.stderr)
        sys.exit(1)

    data = _load_json(cmp_p)
    d768 = data.get("deltas_vs_768") or {}
    deltas = d768.get("deltas") or {}
    inf = d768.get("inference_deltas") or {}

    rois_p = Path(args.rois_json).expanduser().resolve() if args.rois_json else None
    if rois_p is None:
        cand = root / "experiments/yolo/ants_expA004/rois.json"
        rois_p = cand if cand.is_file() else None
    rois_data = _load_json(rois_p) if rois_p and rois_p.is_file() else None
    roi_stats = (rois_data or {}).get("stats") if rois_data else None

    cmp_md = path_for_artifact(cmp_p, root)
    lines: list[str] = [
        "# EXP-A004: ANTS v1 (dense-region refinement)",
        "",
        "Auto-generated by `scripts/evaluation/write_ants_expA004_summary.py`.",
        "",
        "## 1. Method",
        "",
        "- **Stage 1:** full-frame YOLO at `base_imgsz` (see `configs/expA004_ants_v1.yaml`).",
        "- **ROIs:** grid (default) or optional DBSCAN on stage-1 box centers; union dense cells, padded.",
        "- **Stage 2:** crop each ROI, run YOLO at `refine_imgsz`, map boxes to full image.",
        "- **Merge:** concat stage-1 + refined (strategy from config), NMS or union.",
        "- **Eval:** same COCO val GT as EXP-A002b / A003; throughput from `bench_ants_v1.py` embedded via `evaluate.py --inference-benchmark-json`.",
        "",
        "## 2. Inputs",
        "",
        f"- Compare JSON: `{cmp_md}`",
    ]
    paths_echo = data.get("paths") or {}
    for k, v in paths_echo.items():
        lines.append(f"- `{k}`: `{v}`")
    lines.append("")

    if roi_stats:
        lines.extend(
            [
                "### ROI run stats (from rois.json)",
                "",
                f"- Images processed: `{roi_stats.get('n_images_processed', '—')}`",
                f"- Mean ROIs per image: `{_fmt(roi_stats.get('mean_rois_per_image'), 4)}`",
                f"- Mean ROI area fraction of image: `{_fmt(roi_stats.get('mean_roi_area_frac_of_image'), 4)}`",
                "",
            ]
        )

    lines.extend(
        [
            "## 3. Δ vs vanilla imgsz=768 (ANTS − 768)",
            "",
            "| Metric | Δ |",
            "|--------|---|",
            f"| mAP@[.5:.95] | {_fmt(deltas.get('mAP_diff'), 6)} |",
            f"| mAP@.5 | {_fmt(deltas.get('mAP50_diff'), 6)} |",
            f"| mAP_medium | {_fmt(deltas.get('medium_diff'), 6)} |",
            f"| Precision (matched) | {_fmt(deltas.get('precision_diff'), 6)} |",
            f"| Recall (matched) | {_fmt(deltas.get('recall_diff'), 6)} |",
            f"| FPS | {_fmt(inf.get('fps_diff'), 4)} |",
            f"| Latency mean (ms) | {_fmt(inf.get('latency_ms_mean_diff'), 4)} |",
            "",
        ]
    )

    if data.get("deltas_vs_sahi"):
        d2 = (data["deltas_vs_sahi"] or {}).get("deltas") or {}
        inf2 = (data["deltas_vs_sahi"] or {}).get("inference_deltas") or {}
        lines.extend(
            [
                "## 4. Δ vs SAHI (ANTS − SAHI)",
                "",
                "| Metric | Δ |",
                "|--------|---|",
                f"| mAP@[.5:.95] | {_fmt(d2.get('mAP_diff'), 6)} |",
                f"| mAP@.5 | {_fmt(d2.get('mAP50_diff'), 6)} |",
                f"| mAP_medium | {_fmt(d2.get('medium_diff'), 6)} |",
                f"| FPS | {_fmt(inf2.get('fps_diff'), 4)} |",
                "",
            ]
        )
    else:
        lines.extend(
            [
                "## 4. Δ vs SAHI",
                "",
                "*(Skipped — `ants_expA003_sahi_metrics.json` not present when compare was run.)*",
                "",
            ]
        )

    lines.extend(
        [
            f"*Fair comparison note:* {data.get('evaluation_note', '')}",
            "",
            "## 5. Visualizations",
            "",
            "- `experiments/visualizations/ants_expA004/baseline_comparisons/` — TP/FN/FP vs GT for **vanilla 768** preds (if baseline predictions exist).",
            "- `experiments/visualizations/ants_expA004/ants_comparisons/` — same for **ANTS** merged preds.",
            "- `experiments/visualizations/ants_expA004/rois_debug/` — ROI rectangles from `rois.json`.",
            "",
            "## 6. Observations (fill after numbers + viz)",
            "",
            "- Dense-region behavior: duplicate boxes, localization, missed sparse ants outside ROIs.",
            "- mAP / mAP_medium vs 768 and vs SAHI; matched P/R trade-offs.",
            "",
            "## 7. Conclusion",
            "",
            "- State whether ANTS v1 improved, matched, or hurt vs **768** and vs **SAHI** on the metrics you care about.",
            "- Cite throughput cost vs `bench_ants_v1.json` / metrics JSON `inference_benchmark`.",
            "",
            f"- ANTS run id: `{d768.get('compare_experiment_id', '—')}`",
            f"- Baseline 768 id: `{d768.get('baseline_experiment_id', '—')}`",
            "",
        ]
    )

    out_p.parent.mkdir(parents=True, exist_ok=True)
    out_p.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {out_p}")


if __name__ == "__main__":
    main()
