#!/usr/bin/env python3
"""Write experiments/results/ants_expA000_full_summary.md (full run + optional smoke compare)."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any

import yaml


def _load_json(p: Path) -> dict[str, Any]:
    return json.loads(p.read_text(encoding="utf-8"))


def _load_yaml(p: Path) -> dict[str, Any]:
    with open(p, encoding="utf-8") as f:
        return yaml.safe_load(f)


def _fmt(x: Any, digits: int = 6) -> str:
    if isinstance(x, (int, float)) and not isinstance(x, bool):
        if isinstance(x, float) and (x != x):  # nan
            return "nan"
        return f"{x:.{digits}f}" if isinstance(x, float) else str(x)
    return str(x) if x is not None else "—"


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _path_for_md(p: Path, repo_root: Path) -> str:
    """Repo-relative path when under repo root (portable); else absolute."""
    try:
        return str(p.resolve().relative_to(repo_root.resolve())).replace("\\", "/")
    except ValueError:
        return str(p.resolve())


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--metrics-full", type=str, required=True)
    p.add_argument("--metrics-smoke", type=str, default=None, help="Optional smoke metrics for delta table")
    p.add_argument("--relative", type=str, required=True, help="ants_expA000_relative_metrics.json")
    p.add_argument("--train-config", type=str, required=True)
    p.add_argument("--analysis", type=str, required=True)
    p.add_argument("--out", type=str, default="experiments/results/ants_expA000_full_summary.md")
    args = p.parse_args()

    mf = Path(args.metrics_full).expanduser().resolve()
    rel_p = Path(args.relative).expanduser().resolve()
    tc_p = Path(args.train_config).expanduser().resolve()
    an_p = Path(args.analysis).expanduser().resolve()
    out_p = Path(args.out).expanduser().resolve()
    root = _repo_root()

    for path, label in [(mf, "metrics-full"), (rel_p, "relative"), (tc_p, "train-config"), (an_p, "analysis")]:
        if not path.is_file():
            print(f"Missing {label}: {path}", file=sys.stderr)
            sys.exit(1)

    full = _load_json(mf)
    rel = _load_json(rel_p)
    tc = _load_yaml(tc_p)
    an = _load_json(an_p)

    smoke: dict[str, Any] | None = None
    smoke_path: Path | None = None
    if args.metrics_smoke:
        sp = Path(args.metrics_smoke).expanduser().resolve()
        if sp.is_file():
            smoke = _load_json(sp)
            smoke_path = sp

    ce = full.get("coco_eval") or {}
    mpr = full.get("matched_pr") or {}
    bench = full.get("inference_benchmark") or {}
    si = full.get("system_info") or {}
    gt_rel = (rel.get("ground_truth") or {}) if isinstance(rel, dict) else {}
    perc = gt_rel.get("percentiles") or {}

    mopf = an.get("mean_objects_per_frame")
    mopf_s = f"{float(mopf):.4f}" if isinstance(mopf, (int, float)) else "—"

    mf_md = _path_for_md(mf, root)
    rel_md = _path_for_md(rel_p, root)
    tc_md = _path_for_md(tc_p, root)
    an_md = _path_for_md(an_p, root)
    smoke_md = _path_for_md(smoke_path, root) if smoke_path else None

    lines: list[str] = [
        "# EXP-A000 full baseline: Ant detector (YOLO26n)",
        "",
        "Auto-generated by `scripts/evaluation/write_ants_expA000_full_summary.py`.",
        "",
        "## 1. Setup",
        "",
        "| Item | Value |",
        "|------|-------|",
        f"| Run dir (Ultralytics) | `experiments/yolo/ants_expA000_full/` |",
        f"| Metrics JSON | `{mf_md}` |",
        f"| Relative-size JSON | `{rel_md}` |",
        f"| Train config snapshot | `{tc_md}` |",
        f"| Analysis | `{an_md}` |",
        f"| experiment_id | `{full.get('experiment_id', '—')}` |",
        "",
        "### Dataset (from analysis)",
        "",
        f"- **Train images:** {an.get('images', {}).get('train', '—')}",
        f"- **Val images:** {an.get('images', {}).get('val', '—')}",
        f"- **Mean objects / frame:** {mopf_s}",
        f"- **COCO-style small fraction (of ann.):** {an.get('fraction_small_of_annotations', '—')}",
        "",
        "### Training (from run `config.yaml`)",
        "",
        f"- **imgsz:** {tc.get('imgsz', '—')}",
        f"- **epochs:** {tc.get('epochs', '—')}",
        f"- **batch:** {tc.get('batch', '—')}",
        f"- **model:** {tc.get('model', '—')}",
        f"- **data:** {tc.get('data', '—')}",
        "",
        "**Stack (from metrics JSON):**",
        "",
        f"- torch: {si.get('torch', '—')}",
        f"- cuda_device_0: {si.get('cuda_device_0', '—')}",
        "",
        "## 2. Primary metrics (validation)",
        "",
        "| Metric | Value |",
        "|--------|------:|",
        f"| mAP@[.50:.95] | {_fmt(ce.get('mAP_50_95'))} |",
        f"| mAP@.50 | {_fmt(ce.get('mAP_50'))} |",
        f"| mAP_medium | {_fmt(ce.get('mAP_medium'))} |",
        f"| mAP_small | {_fmt(ce.get('mAP_small'))} |",
        f"| mAP_large | {_fmt(ce.get('mAP_large'))} |",
        f"| Precision (IoU≥0.5, score≥0.25) | {_fmt(mpr.get('precision_iou50_score025'))} |",
        f"| Recall (same) | {_fmt(mpr.get('recall_iou50_score025'))} |",
        f"| FPS | {_fmt(bench.get('fps'), 2)} |",
        f"| Latency mean (ms) | {_fmt(bench.get('latency_ms_mean'), 3)} |",
        "",
        "## 3. COCO small / large and relative size",
        "",
        "- **mAP_small / mAP_large = -1:** pycocotools reports -1 when there are **no ground-truth boxes** in the corresponding **absolute pixel-area** buckets (COCO definitions), not when objects are visually small in the frame.",
        "- **Relative “small on frame”** is summarized separately in `experiments/results/ants_expA000_relative_metrics.json` (bbox area ÷ image area). See also `docs/research_analysis.md`.",
        "",
        f"**GT relative area (percentiles):** p5={_fmt(perc.get('p5'))}, p50={_fmt(perc.get('p50'))}, p95={_fmt(perc.get('p95'))}.",
        "",
        f"Full relative stats: `{rel_p}`",
        "",
        "## 4. Observations",
        "",
        "- Ants are often **tiny relative to 1920×1080**; matched precision/recall at IoU 0.5 are practical sanity checks alongside mAP.",
        "- Overlays: `experiments/visualizations/ants_expA000_full/` (`predictions/`, `comparisons/`) after `run_ants_expA000_full.sh`.",
        "- **vs COCO128 (EXP-000):** different domain; absolute mAP is not directly comparable.",
        "",
    ]

    if smoke and smoke_path:
        sce = smoke.get("coco_eval") or {}
        smpr = smoke.get("matched_pr") or {}
        sb = smoke.get("inference_benchmark") or {}

        def _d(a: Any, b: Any) -> str:
            if not isinstance(a, (int, float)) or not isinstance(b, (int, float)):
                return "—"
            if a != a or b != b:
                return "—"
            return _fmt(float(b) - float(a))

        lines.extend(
            [
                "## 5. Smoke vs full (delta: full − smoke)",
                "",
                f"Smoke metrics: `{smoke_md}`",
                "",
                "| Metric | Smoke | Full | Δ (full − smoke) |",
                "|--------|------:|-----:|-----------------:|",
                f"| mAP@[.50:.95] | {_fmt(sce.get('mAP_50_95'))} | {_fmt(ce.get('mAP_50_95'))} | "
                f"{_d(sce.get('mAP_50_95'), ce.get('mAP_50_95'))} |",
                f"| mAP@.50 | {_fmt(sce.get('mAP_50'))} | {_fmt(ce.get('mAP_50'))} | "
                f"{_d(sce.get('mAP_50'), ce.get('mAP_50'))} |",
                f"| mAP_medium | {_fmt(sce.get('mAP_medium'))} | {_fmt(ce.get('mAP_medium'))} | "
                f"{_d(sce.get('mAP_medium'), ce.get('mAP_medium'))} |",
                f"| Precision (IoU≥0.5) | {_fmt(smpr.get('precision_iou50_score025'))} | "
                f"{_fmt(mpr.get('precision_iou50_score025'))} | "
                f"{_d(smpr.get('precision_iou50_score025'), mpr.get('precision_iou50_score025'))} |",
                f"| Recall (IoU≥0.5) | {_fmt(smpr.get('recall_iou50_score025'))} | "
                f"{_fmt(mpr.get('recall_iou50_score025'))} | "
                f"{_d(smpr.get('recall_iou50_score025'), mpr.get('recall_iou50_score025'))} |",
                f"| FPS | {_fmt(sb.get('fps'), 2)} | {_fmt(bench.get('fps'), 2)} | "
                f"{_d(sb.get('fps'), bench.get('fps'))} |",
                f"| Latency mean (ms) | {_fmt(sb.get('latency_ms_mean'), 3)} | {_fmt(bench.get('latency_ms_mean'), 3)} | "
                f"{_d(sb.get('latency_ms_mean'), bench.get('latency_ms_mean'))} |",
                "",
            ]
        )
    else:
        lines.extend(
            [
                "## 5. Smoke vs full",
                "",
                "No smoke metrics path provided or file missing; re-run with `--metrics-smoke experiments/results/ants_expA000_smoke_metrics.json` after `./scripts/run_ants_expA000_smoke.sh`.",
                "",
            ]
        )

    lines.extend(
        [
            "## 6. Next steps",
            "",
            "- **EXP-A002b:** Resolution sweep (`imgsz` grid) on `datasets/ants_yolo`.",
            "- **EXP-A003:** SAHI sliced inference vs vanilla on this val set.",
            "- **EXP-A004:** ANTS method (to be defined).",
            "",
        ]
    )

    out_p.parent.mkdir(parents=True, exist_ok=True)
    out_p.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {out_p}")


if __name__ == "__main__":
    main()
