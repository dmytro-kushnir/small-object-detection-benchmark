#!/usr/bin/env python3
"""Write experiments/results/ants_expA003_summary.md from compare_metrics JSON + optional metric files."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any


def _load_json(p: Path) -> dict[str, Any]:
    return json.loads(p.read_text(encoding="utf-8"))


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _path_for_md(p: Path, repo_root: Path) -> str:
    try:
        return str(p.resolve().relative_to(repo_root.resolve())).replace("\\", "/")
    except ValueError:
        return str(p.resolve())


def _fmt(x: Any, digits: int = 4) -> str:
    if isinstance(x, (int, float)) and not isinstance(x, bool):
        if isinstance(x, float) and (x != x):
            return "nan"
        return f"{x:.{digits}f}" if isinstance(x, float) else str(x)
    return str(x) if x is not None else "—"


def _row_from_metrics(m: dict[str, Any]) -> dict[str, Any]:
    ce = m.get("coco_eval") or {}
    pr = m.get("matched_pr") or {}
    ib = m.get("inference_benchmark") or {}
    return {
        "mAP": ce.get("mAP_50_95"),
        "mAP50": ce.get("mAP_50"),
        "mAP_medium": ce.get("mAP_medium"),
        "precision": pr.get("precision_iou50_score025"),
        "recall": pr.get("recall_iou50_score025"),
        "fps": ib.get("fps"),
        "latency_ms": ib.get("latency_ms_mean"),
    }


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument(
        "--compare",
        type=str,
        required=True,
        help="ants_expA003_vs_768.json from compare_metrics.py",
    )
    p.add_argument("--baseline-metrics", type=str, default=None)
    p.add_argument("--sahi-metrics", type=str, default=None)
    p.add_argument(
        "--out",
        type=str,
        default="experiments/results/ants_expA003_summary.md",
    )
    args = p.parse_args()

    cmp_p = Path(args.compare).expanduser().resolve()
    out_p = Path(args.out).expanduser().resolve()
    root = _repo_root()

    if not cmp_p.is_file():
        print(f"Missing compare JSON: {cmp_p}", file=sys.stderr)
        sys.exit(1)

    data = _load_json(cmp_p)
    deltas = data.get("deltas") or {}
    inf = data.get("inference_deltas") or {}

    base_p = Path(args.baseline_metrics).expanduser().resolve() if args.baseline_metrics else None
    sahi_p = Path(args.sahi_metrics).expanduser().resolve() if args.sahi_metrics else None
    base_m = _load_json(base_p) if base_p and base_p.is_file() else None
    sahi_m = _load_json(sahi_p) if sahi_p and sahi_p.is_file() else None

    cmp_md = _path_for_md(cmp_p, root)
    base_md = _path_for_md(base_p, root) if base_p and base_p.is_file() else None
    sahi_md = _path_for_md(sahi_p, root) if sahi_p and sahi_p.is_file() else None

    lines: list[str] = [
        "# EXP-A003: Ants SAHI vs vanilla imgsz=768",
        "",
        "Auto-generated by `scripts/evaluation/write_ants_expA003_summary.py`.",
        "",
        "## 1. Inputs",
        "",
        f"- Compare JSON: `{cmp_md}`",
    ]
    if base_md:
        lines.append(f"- Vanilla 768 baseline metrics: `{base_md}` (`ants_expA002b_imgsz768`)")
    if sahi_md:
        lines.append(f"- SAHI metrics: `{sahi_md}`")
    lines.extend(
        [
            "",
            "## Visual overlays",
            "",
            "PNG images from `scripts/visualization/viz_coco_overlays.py` (also invoked at the end of `scripts/run_ants_expA003.sh`).",
            "",
            "- `experiments/visualizations/ants_expA003_sahi/predictions/` — predicted boxes only",
            "- `experiments/visualizations/ants_expA003_sahi/comparisons/` — TP / FN (orange, thick) / FP (red, thick); greedy match IoU≥0.5, score≥0.25",
            "- `experiments/visualizations/ants_expA003_sahi/dataset/` — random GT-only samples",
            "",
            "Re-run viz only (default 250 images):",
            "",
            "```bash",
            "python3 scripts/visualization/viz_coco_overlays.py \\",
            "  --pred experiments/yolo/ants_expA003_sahi/predictions_val.json \\",
            "  --gt datasets/ants_yolo/annotations/instances_val.json \\",
            "  --images-dir datasets/ants_yolo/images/val \\",
            "  --out-dir experiments/visualizations/ants_expA003_sahi \\",
            "  --max-images 250",
            "```",
            "",
            "Full val set: `ANTS_VIZ_MAX_IMAGES=all ./scripts/run_ants_expA003.sh` (re-runs infer+eval too) or pass `--max-images` equal to val image count.",
            "",
            "## 2. Comparison vs imgsz=768 baseline",
            "",
            "**Δ = SAHI − vanilla** (from `compare_metrics.py`).",
            "",
            "| Metric | Δ |",
            "|--------|---|",
            f"| mAP@[.5:.95] | {_fmt(deltas.get('mAP_diff'), 6)} |",
            f"| mAP@.5 | {_fmt(deltas.get('mAP50_diff'), 6)} |",
            f"| mAP_medium | {_fmt(deltas.get('medium_diff'), 6)} |",
            f"| Precision (matched, IoU≥0.5) | {_fmt(deltas.get('precision_diff'), 6)} |",
            f"| Recall (matched) | {_fmt(deltas.get('recall_diff'), 6)} |",
            f"| FPS (`evaluate.py` bench) | {_fmt(inf.get('fps_diff'), 4)} |",
            f"| Latency mean (ms) | {_fmt(inf.get('latency_ms_mean_diff'), 4)} |",
            "",
        ]
    )

    if base_m and sahi_m:
        rb = _row_from_metrics(base_m)
        rs = _row_from_metrics(sahi_m)
        lines.extend(
            [
                "### Absolute values (reference)",
                "",
                "| Metric | Vanilla 768 | SAHI |",
                "|--------|------------:|-----:|",
                f"| mAP@[.5:.95] | {_fmt(rb['mAP'])} | {_fmt(rs['mAP'])} |",
                f"| mAP@.5 | {_fmt(rb['mAP50'])} | {_fmt(rs['mAP50'])} |",
                f"| mAP_medium | {_fmt(rb['mAP_medium'])} | {_fmt(rs['mAP_medium'])} |",
                f"| Precision | {_fmt(rb['precision'])} | {_fmt(rs['precision'])} |",
                f"| Recall | {_fmt(rb['recall'])} | {_fmt(rs['recall'])} |",
                f"| FPS | {_fmt(rb['fps'], 2)} | {_fmt(rs['fps'], 2)} |",
                f"| Latency ms | {_fmt(rb['latency_ms'], 2)} | {_fmt(rs['latency_ms'], 2)} |",
                "",
            ]
        )

    lines.extend(
        [
            f"*Fair comparison note:* {data.get('evaluation_note', '')}",
            "",
            "## 3. Impact (read from Δ above)",
            "",
            "- **Detection accuracy / localization:** mAP@[.5:.95] and **mAP_medium** (primary COCO bucket for ants; mAP_small often −1). Positive Δ → better average localization under COCO.",
            "- **Recall / precision (matched):** greedy match at IoU≥0.5, score≥0.25 — higher recall may mean fewer missed ants in dense frames; precision drops if SAHI adds overlapping false boxes.",
            "- **Speed:** FPS and latency rows mix **vanilla `predict`** (baseline) vs **SAHI sliced** path (SAHI); use as indicative, not apples-to-apples micro-benchmarks.",
            "",
            "## 4. Analysis prompts",
            "",
            "- **Localization:** If mAP@[.5:.95] rises but mAP@.5 is flat, SAHI may be tightening box alignment. If both move together, check matched P/R and viz `comparisons/` (orange FN, thick red FP).",
            "- **Dense scenes:** Compare FN counts visually under `experiments/visualizations/ants_expA003_sahi/comparisons/` vs `ants_expA002b/imgsz768/`.",
            "- **Trade-off:** Weigh any mAP gain vs FPS/latency cost for deployment (re-bench on edge hardware if needed).",
            "",
            "## 5. Conclusion (fill after inspecting numbers and viz)",
            "",
            "- **Is SAHI useful for ants here?** (Yes / marginal / no — cite mAP_medium and P/R deltas.)",
            "- **Is optimized 768 alone sufficient?** If SAHI Δ ≈ 0 or negative on mAP with slower FPS, prefer vanilla 768.",
            "",
            f"- Baseline run: `{data.get('baseline_experiment_id', '—')}`",
            f"- SAHI run: `{data.get('compare_experiment_id', '—')}`",
            "",
        ]
    )

    out_p.parent.mkdir(parents=True, exist_ok=True)
    out_p.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {out_p}")


if __name__ == "__main__":
    main()
