#!/usr/bin/env python3
"""Write experiments/results/ants_expA000_summary.md from analysis + metrics + train config."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any

import yaml


def _load_json(p: Path) -> dict[str, Any]:
    return json.loads(p.read_text(encoding="utf-8"))


def _load_yaml(p: Path) -> dict[str, Any]:
    with open(p, encoding="utf-8") as f:
        return yaml.safe_load(f)


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--analysis", type=str, required=True)
    p.add_argument("--metrics", type=str, required=True)
    p.add_argument("--train-config", type=str, required=True)
    p.add_argument("--out", type=str, default="experiments/results/ants_expA000_summary.md")
    args = p.parse_args()

    analysis_path = Path(args.analysis).expanduser().resolve()
    metrics_path = Path(args.metrics).expanduser().resolve()
    tc_path = Path(args.train_config).expanduser().resolve()
    out_path = Path(args.out).expanduser().resolve()

    if not analysis_path.is_file():
        print(f"Missing analysis: {analysis_path}", file=sys.stderr)
        sys.exit(1)
    if not metrics_path.is_file():
        print(f"Missing metrics: {metrics_path}", file=sys.stderr)
        sys.exit(1)
    if not tc_path.is_file():
        print(f"Missing train config: {tc_path}", file=sys.stderr)
        sys.exit(1)

    an = _load_json(analysis_path)
    met = _load_json(metrics_path)
    tc = _load_yaml(tc_path)

    ce = met.get("coco_eval") or {}
    mpr = met.get("matched_pr") or {}
    bench = met.get("inference_benchmark") or {}
    si = met.get("system_info") or {}

    mopf = an.get("mean_objects_per_frame")
    mopf_s = f"{float(mopf):.4f}" if isinstance(mopf, (int, float)) else "—"

    lines = [
        "# EXP-A000: Ant detector baseline (YOLO26n)",
        "",
        "Auto-generated by `scripts/evaluation/write_ants_expA000_summary.py`. Re-run after refreshing metrics.",
        "",
        "## 1. Dataset summary",
        "",
        f"- **Train images:** {an.get('images', {}).get('train', '—')}",
        f"- **Val images:** {an.get('images', {}).get('val', '—')}",
        f"- **Total annotations (train+val):** {an.get('annotations_total', '—')}",
        f"- **Mean objects / frame:** {mopf_s}",
        f"- **BBox side (px) mean:** {an.get('bbox_side_px_mean', '—')}",
        f"- **COCO-style small fraction (of ann.):** "
        f"{an.get('fraction_small_of_annotations', '—')}",
        "",
        f"Full stats: `{analysis_path}`",
        "",
        "## 2. Training setup",
        "",
        f"- **imgsz:** {tc.get('imgsz', '—')}",
        f"- **epochs:** {tc.get('epochs', '—')}",
        f"- **batch:** {tc.get('batch', '—')}",
        f"- **model:** {tc.get('model', '—')}",
        f"- **data:** {tc.get('data', '—')}",
        "",
        "**Hardware / stack (from metrics JSON):**",
        "",
        f"- torch: {si.get('torch', '—')}",
        f"- cuda_available: {si.get('cuda_available', '—')}",
        f"- cuda_device_0: {si.get('cuda_device_0', '—')}",
        "",
        f"Frozen config: `{tc_path}`",
        "",
        "## 3. Metrics (validation)",
        "",
        "| Metric | Value |",
        "|--------|------:|",
        f"| mAP@[.50:.95] | {ce.get('mAP_50_95', '—')} |",
        f"| mAP@.50 | {ce.get('mAP_50', '—')} |",
        f"| mAP_small | {ce.get('mAP_small', '—')} |",
        f"| mAP_medium | {ce.get('mAP_medium', '—')} |",
        f"| mAP_large | {ce.get('mAP_large', '—')} |",
        f"| Precision (IoU≥0.5, score≥0.25) | {mpr.get('precision_iou50_score025', '—')} |",
        f"| Recall (same) | {mpr.get('recall_iou50_score025', '—')} |",
        f"| FPS | {bench.get('fps', '—')} |",
        f"| Latency mean (ms) | {bench.get('latency_ms_mean', '—')} |",
        "",
        f"Source: `{metrics_path}` (`experiment_id`: {met.get('experiment_id', '—')})",
        "",
        "## 4. Observations",
        "",
        "- **Small objects:** Ants are typically tiny in 1920×1080 frames; check `mAP_small` and `fraction_small_of_annotations` in `analysis.json`.",
        "- **Qualitative:** See `experiments/visualizations/ants_dataset/` (GT samples) and prediction overlays after you run `viz_coco_overlays.py` on ant preds if desired.",
        "- **vs COCO128 smoke (EXP-000):** Different domain and label noise; do not compare absolute mAP across datasets without caveats.",
        "",
        "## 5. Next steps",
        "",
        "- **EXP-A002b:** Resolution sweep (`imgsz` grid) on `datasets/ants_yolo` (same temporal split).",
        "- **EXP-A003:** SAHI sliced inference (no retrain) vs vanilla predict on this val set.",
        "- **EXP-A004:** ANTS method (to be defined).",
        "",
    ]

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {out_path}")


if __name__ == "__main__":
    main()
