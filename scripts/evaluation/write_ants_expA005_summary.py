#!/usr/bin/env python3
"""Write experiments/results/ants_expA005_rfdetr_summary.md from compare JSON + config paths."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any


def _load_json(p: Path) -> dict[str, Any]:
    return json.loads(p.read_text(encoding="utf-8"))


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _rel(p: Path, root: Path) -> str:
    try:
        return str(p.resolve().relative_to(root.resolve())).replace("\\", "/")
    except ValueError:
        return str(p.resolve())


def _fmt(x: Any, digits: int = 4) -> str:
    if isinstance(x, float) and x != x:
        return "nan"
    if isinstance(x, float):
        return f"{x:.{digits}f}"
    return str(x) if x is not None else "—"


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument(
        "--compare",
        type=str,
        default="experiments/results/ants_expA005_rfdetr_vs_yolo.json",
    )
    p.add_argument(
        "--exp-config",
        type=str,
        default="configs/expA005_ants_rfdetr.yaml",
        help="EXP-A005 YAML (paths for doc only)",
    )
    p.add_argument(
        "--out",
        type=str,
        default="experiments/results/ants_expA005_rfdetr_summary.md",
    )
    args = p.parse_args()

    cmp_p = Path(args.compare).expanduser().resolve()
    out_p = Path(args.out).expanduser().resolve()
    root = _repo_root()

    if not cmp_p.is_file():
        print(f"Missing compare JSON: {cmp_p}", file=sys.stderr)
        sys.exit(1)

    data = _load_json(cmp_p)
    pair = data.get("deltas_rfdetr_minus_yolo") or {}
    deltas = pair.get("deltas") or {}
    inf = pair.get("inference_deltas") or {}
    vb = pair.get("baseline_metrics") or {}
    vc = pair.get("compare_metrics") or {}

    lines = [
        "# EXP-A005: RF-DETR vs YOLO26 (ants val)",
        "",
        "Auto-generated by `scripts/evaluation/write_ants_expA005_summary.py`.",
        "",
        "## 1. Model",
        "",
        "- **RF-DETR** fine-tuned on `datasets/ants_coco` (same split as `ants_yolo`).",
        "- **Baseline:** YOLO26 imgsz=768 (`ants_expA002b_imgsz768`).",
        "- See resolved training config under `experiments/rfdetr/ants_expA005/config.yaml` after training.",
        "",
        "## 2. Inputs",
        "",
        f"- Compare JSON: `{_rel(cmp_p, root)}`",
        f"- EXP config template: `{_rel(Path(args.exp_config), root)}`",
    ]
    for k, v in (data.get("paths") or {}).items():
        lines.append(f"- `{k}`: `{v}`")
    lines.extend(
        [
            "",
            "## 3. Metrics (absolute)",
            "",
            "| Metric | YOLO768 | RF-DETR |",
            "|--------|--------:|--------:|",
            f"| mAP@[.5:.95] | {_fmt(vb.get('mAP_50_95'), 4)} | {_fmt(vc.get('mAP_50_95'), 4)} |",
            f"| mAP@.5 | {_fmt(vb.get('mAP_50'), 4)} | {_fmt(vc.get('mAP_50'), 4)} |",
            f"| mAP_medium | {_fmt(vb.get('mAP_medium'), 4)} | {_fmt(vc.get('mAP_medium'), 4)} |",
            f"| Matched P | {_fmt(vb.get('precision'), 4)} | {_fmt(vc.get('precision'), 4)} |",
            f"| Matched R | {_fmt(vb.get('recall'), 4)} | {_fmt(vc.get('recall'), 4)} |",
            "",
            "## 4. Δ (RF-DETR − YOLO768)",
            "",
            "| Metric | Δ |",
            "|--------|---|",
            f"| mAP@[.5:.95] | {_fmt(deltas.get('mAP_diff'), 6)} |",
            f"| mAP@.5 | {_fmt(deltas.get('mAP50_diff'), 6)} |",
            f"| mAP_medium | {_fmt(deltas.get('medium_diff'), 6)} |",
            f"| Precision | {_fmt(deltas.get('precision_diff'), 6)} |",
            f"| Recall | {_fmt(deltas.get('recall_diff'), 6)} |",
        ]
    )
    if inf.get("fps_diff") is not None:
        lines.append(f"| FPS | {_fmt(inf.get('fps_diff'), 4)} |")
    if inf.get("latency_ms_mean_diff") is not None:
        lines.append(f"| Latency mean (ms) | {_fmt(inf.get('latency_ms_mean_diff'), 4)} |")
    lines.extend(
        [
            "",
            "## 5. Observations (fill after review)",
            "",
            "- **Duplicates / FP:** compare matched FP counts in compare JSON; inspect `side_by_side/` viz.",
            "- **Dense scenes:** check frames with high object count in YOLO vs RF-DETR panels.",
            "- **Localization:** mAP@[.5:.95] reflects IoU quality, not just hit rate.",
            "",
            "## 6. Conclusion",
            "",
            "- Summarize which architecture is stronger on this val set and at what throughput cost.",
            "- Note resolution / training budget differences vs YOLO768 before claiming architecture effect.",
            "",
        ]
    )

    out_p.parent.mkdir(parents=True, exist_ok=True)
    out_p.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {out_p}")


if __name__ == "__main__":
    main()
